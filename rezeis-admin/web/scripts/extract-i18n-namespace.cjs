#!/usr/bin/env node
/**
 * Extracts top-level i18n namespaces from src/i18n/{ru,en}.ts into a
 * separate "feature" set under src/i18n/features/<name>{.ru,.en,}.ts.
 *
 * Output layout:
 *   src/i18n/features/<feature>.ru.ts   — `export const ru = { ... }`
 *   src/i18n/features/<feature>.en.ts   — `export const en = { ... }`
 *   src/i18n/features/<feature>.ts      — re-exports both
 *
 * The split lets Vite emit one chunk per language, so only the active
 * locale ships to the user when the feature is loaded.
 *
 * Usage:
 *   node scripts/extract-i18n-namespace.cjs <feature-name> <ns1> [<ns2> ...]
 *
 * Strategy:
 *   1. Locate top-level keys by `^  <ident>:\s` lines (2-space indent).
 *   2. Find the matching closing `^  },` line at the same indent (for objects)
 *      or treat single-line scalars as their own slice.
 *   3. Collect those slices, write per-language feature files, and delete
 *      them from the source.
 */

const fs = require('node:fs');
const path = require('node:path');

const args = process.argv.slice(2);
if (args.length < 2) {
  console.error('Usage: extract-i18n-namespace.cjs <feature-name> <ns1> [<ns2> ...]');
  process.exit(1);
}

const [featureName, ...namespaces] = args;

const ROOT = path.join(__dirname, '..');
const I18N_DIR = path.join(ROOT, 'src', 'i18n');
const FEATURES_DIR = path.join(I18N_DIR, 'features');

if (!fs.existsSync(FEATURES_DIR)) {
  fs.mkdirSync(FEATURES_DIR, { recursive: true });
}

function findNamespaceRanges(content, namespaces) {
  const lines = content.split('\n');
  const ranges = []; // { name, start, end } — inclusive

  const TOP_KEY_RE = /^ {2}([A-Za-z_][A-Za-z0-9_]*):\s/;

  for (let i = 0; i < lines.length; i++) {
    const m = TOP_KEY_RE.exec(lines[i]);
    if (!m) continue;
    if (!namespaces.includes(m[1])) continue;

    const startLine = i;
    const startContent = lines[i].trimEnd();
    if (startContent.endsWith('{')) {
      for (let j = i + 1; j < lines.length; j++) {
        if (/^ {2}},?\s*$/.test(lines[j])) {
          ranges.push({ name: m[1], start: startLine, end: j });
          i = j;
          break;
        }
      }
    } else {
      ranges.push({ name: m[1], start: startLine, end: startLine });
    }
  }

  return ranges;
}

function extractFromFile(filePath, locale) {
  const content = fs.readFileSync(filePath, 'utf8');
  const lines = content.split('\n');
  const ranges = findNamespaceRanges(content, namespaces);

  if (ranges.length !== namespaces.length) {
    const found = ranges.map((r) => r.name);
    const missing = namespaces.filter((n) => !found.includes(n));
    if (missing.length > 0) {
      console.warn(`[${locale}] Missing namespaces: ${missing.join(', ')}`);
    }
  }

  const slices = [];
  for (const ns of namespaces) {
    const r = ranges.find((rr) => rr.name === ns);
    if (!r) continue;
    const sliceLines = lines.slice(r.start, r.end + 1);
    const last = sliceLines[sliceLines.length - 1];
    if (!last.trimEnd().endsWith(',')) {
      sliceLines[sliceLines.length - 1] = last.replace(/(\s*)$/, ',$1');
    }
    slices.push(sliceLines.join('\n'));
  }

  const sortedRanges = [...ranges].sort((a, b) => b.start - a.start);
  for (const r of sortedRanges) {
    lines.splice(r.start, r.end - r.start + 1);
  }

  fs.writeFileSync(filePath, lines.join('\n'), 'utf8');

  return slices.join('\n');
}

const ruSlices = extractFromFile(path.join(I18N_DIR, 'ru.ts'), 'ru');
const enSlices = extractFromFile(path.join(I18N_DIR, 'en.ts'), 'en');

// Per-language module: ru
const ruFile = `/**
 * Lazy-loaded i18n feature bundle (ru): ${featureName}
 *
 * Generated by scripts/extract-i18n-namespace.cjs.
 * Contains namespaces: ${namespaces.join(', ')}.
 */

export const ru = {
${ruSlices}
} as const
`;
fs.writeFileSync(path.join(FEATURES_DIR, `${featureName}.ru.ts`), ruFile, 'utf8');

// Per-language module: en
const enFile = `/**
 * Lazy-loaded i18n feature bundle (en): ${featureName}
 *
 * Generated by scripts/extract-i18n-namespace.cjs.
 * Contains namespaces: ${namespaces.join(', ')}.
 */

export const en = {
${enSlices}
} as const
`;
fs.writeFileSync(path.join(FEATURES_DIR, `${featureName}.en.ts`), enFile, 'utf8');

// Aggregator (so callers can import either both or one language).
const aggregatorFile = `/**
 * Lazy-loaded i18n feature bundle: ${featureName}
 *
 * Re-exports per-language modules so Vite can split each language into
 * its own chunk; only the active language ships when the feature loads.
 *
 * Contains namespaces: ${namespaces.join(', ')}.
 */

import { ru } from './${featureName}.ru'
import { en } from './${featureName}.en'

export const ${featureName} = { ru, en } as const
`;
fs.writeFileSync(path.join(FEATURES_DIR, `${featureName}.ts`), aggregatorFile, 'utf8');

console.log(`✓ Extracted [${namespaces.join(', ')}] → src/i18n/features/${featureName}.{ru,en,}.ts`);
console.log(`✓ Removed slices from ru.ts and en.ts`);
